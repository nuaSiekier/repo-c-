using System;
using System.Collections.Generic;
using System.Text;

namespace Project1
{
    public class MathOperations
    {
        public int Add(int x, int y, int z)
        {
            return x + y + z;
        }

        public int Sub(int x, int y)
        {
            return x - y;
        }

        public double Imp(double x, double y)
        {
            return x * y;
        }

      }
}
